package org.adams.training.java.abstraction;

// Abstract Class can be a combination of concrete and abstract methods.
public abstract class AbstractClassConcreteAbstractMethods {
	
	void methodOne()
    {
        //Concrete Method
    }
 
    void methodTwo()
    {
        //Concrete Method
    }
 
    abstract void methodThree();  //Abstract Method
 
    abstract void methodFour();  //Abstract Method

}

